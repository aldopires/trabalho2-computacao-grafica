    public void desenha() {
        try {
            Objeto2d o = controlador.Controle.getPerfil2d();
            if (!jButtonExtrusao.isEnabled() && !o.isEmpy()) {
                jButtonExtrusao.setEnabled(true);
                jButtonRevolucao.setEnabled(true);
                jRadioButtonEscala.setEnabled(true);
                jRadioButtonMover.setEnabled(true);
                jRadioButtonRotacionar.setEnabled(true);
            }
            Graphics g = jPanel1.getGraphics();
            g.setColor(Color.white);
            g.fillRect(0, 0, jPanel1.getWidth(), jPanel1.getHeight());
            g.setColor(Color.BLACK);
            for (Aresta2d aresta : o.getArestas()) {
                g.drawLine(
                        (int) aresta.getP1().getX(),
                        (int) aresta.getP1().getY(),
                        (int) aresta.getP2().getX(),
                        (int) aresta.getP2().getY());
            }
        } catch (Exception e) {
        }
    }

    @Override
    public void paint(Graphics g) {
        super.paint(g);
        desenha();
    }
