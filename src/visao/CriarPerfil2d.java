    private void jButtonRevolucaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRevolucaoActionPerformed
        // TODO add your handling code here:
        int grid = -1;

        String s;
        for (;;) {
            try {
                s = JOptionPane.showInputDialog(rootPane, "Grid: ");
                if (s != null) {
                    grid = Integer.parseInt(s);
                    if (grid > 0) {
                        break;
                    }
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Valor digitado incorreto!");
                e.printStackTrace();
            }
        }
        if (s != null && grid > 0) {
            controlador.Controle.revolucao(grid);
            dispose();
        }
    }//GEN-LAST:event_jButtonRevolucaoActionPerformed

